print("Curved SUrface Area of a Cylinder")
radius = int(input("Enter Radius (in cm) : "))
height = int(input("Enter Height (in cm) : "))
pi = 3.14
CSA = (2 * pi * radius) * height
print("\nRadius : " + str(radius) +"cm")
print("Height : " + str(height) +"cm")
print("Curved Surface Area of a Cylinder is " + str(CSA) +" sq.cm")

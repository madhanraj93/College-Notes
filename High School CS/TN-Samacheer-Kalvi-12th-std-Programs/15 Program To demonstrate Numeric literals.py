# Program to demonstrate Numeric Literals
a = 0b1010
# Binary Literals
b = 100
# Decimal Literal
c = 0o310
# Octal Literal
d = 0x12c
# Hexadecimal Literal
print("Integer Literals :", a, b, c, d)
# Float Literal
float_1 = 10.5
float_2 = 1.5e2
print("Float Literals :", float_1, float_2)
# Complex Literal
x = 1 + 3.14j
print("Complex Literals :", x)
print("x = ", x, "Imaginary part of x = ", x.imag, "Real part of x = ", x.real)
# End of the Program

# Demo Program to test Assignment Operators
x = int(input("Type a Value for X : "))
print("X = ", x)
print("The x is =", x)
x += 20
print("The x += 20 is =", x)
x -= 5
print("The x -= 5 is = ", x)
x *= 5
print("The x *= 5 is = ", x)
x /= 2
print("The x /= 2 is = ", x)
x %= 3
print("The x %= 3 is = ", x)
x **= 2
print("The x **= 2 is = ", x)
x //= 3
print("The x //= 3 is = ", x)
